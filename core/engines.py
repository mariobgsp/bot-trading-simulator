# ENGINE 1: FVG PULLBACK
# ══════════════════════════════════════════════════════════════════════════════


class FVGPullbackEngine(BaseEngine):
    """
    Fair Value Gap Pullback Entry Engine.

    Identifies a 3-candle bullish FVG in an uptrend, then triggers
    a buy when price drops into the FVG zone on low volume and
    prints a higher daily close.

    Conditions:
    1. Stock must be in an uptrend (price > SMA50)
    2. A recent unfilled bullish FVG must exist
    3. Previous day's low penetrated or touched the FVG zone
    4. Volume on the pullback day was below average (< 80%)
    5. Today's close is higher than previous close (reversal)

    Priority: 3 (highest)
    Allowed regimes: BULL, CAUTION
    """

    name = "fvg_pullback"
    priority = 3

    def scan(
        self,
        df: pd.DataFrame,
        ticker: str,
        regime: RegimeSnapshot,
        profile: "StockProfile | None" = None,
    ) -> EntrySignal | None:
        # Regime gate
        if not regime.allows_engine(self.name):
            return None

        if len(df) < max(REGIME_SMA_SHORT + 10, 60):
            return None

        last = df.iloc[-1]
        prev = df.iloc[-2]
        last_close = float(last["Close"])
        prev_close = float(prev["Close"])
        prev_low = float(prev["Low"])
        last_open = float(last["Open"])

        # 1. Uptrend check: price above SMA(50)
        sma50 = sma(df["Close"], REGIME_SMA_SHORT)
        if pd.isna(sma50.iloc[-1]) or last_close <= float(sma50.iloc[-1]):
            return None

        # 2. Find recent unfilled bullish FVGs (last 90 calendar days)
        fvgs = detect_fvg(df)
        recent_bullish = [
            g for g in fvgs
            if g.gap_type == "bullish"
            and not g.filled
            and (df.index[-1] - g.date).days <= 90
        ]

        if not recent_bullish:
            return None

        # 3. Volume must be below average on the pullback (previous day)
        vol_ratio = volume_ratio(df, period=20)
        prev_vol_ratio = float(vol_ratio.iloc[-2]) if not pd.isna(vol_ratio.iloc[-2]) else 1.0

        # 4. Check each FVG for a pullback entry
        atr_series = atr(df, period=ATR_PERIOD)
        last_atr = float(atr_series.iloc[-1]) if not pd.isna(atr_series.iloc[-1]) else 0.0

        for gap in recent_bullish:
            # Previous day's low penetrated or touched the FVG zone
            if prev_low <= gap.gap_high:
                # Low volume on pullback day
                if prev_vol_ratio < FVG_LOW_VOLUME_RATIO:
                    # Today's close is higher (reversal confirmation)
                    if last_close > prev_close and last_close > last_open:
                        last_vol_ratio = float(vol_ratio.iloc[-1]) if not pd.isna(vol_ratio.iloc[-1]) else 1.0
                        score = self.priority * max(last_vol_ratio, 1.0)

                        return EntrySignal(
                            engine=self.name,
                            ticker=ticker,
                            price=round(last_close, 2),
                            score=round(score, 2),
                            priority=self.priority,
                            details={
                                "fvg_date": gap.date.strftime("%Y-%m-%d"),
                                "gap_high": round(gap.gap_high, 2),
                                "gap_low": round(gap.gap_low, 2),
                                "gap_size": round(gap.gap_size, 2),
                                "pullback_vol_ratio": round(prev_vol_ratio, 2),
                                "reversal_vol_ratio": round(last_vol_ratio, 2),
                                "atr": round(last_atr, 2),
                                "sma50": round(float(sma50.iloc[-1]), 2),
                            },
                        )

        return None


