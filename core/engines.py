def run_all_engines(
    df: pd.DataFrame,
    ticker: str,
    regime: RegimeSnapshot,
    profile: "StockProfile | None" = None,
) -> list[EntrySignal]:
    """
    Run all engines against a single stock and collect signals.

    Returns a list of all signals that fired (usually 0 or 1).
    Multiple engines can fire on the same stock.
    """
    signals: list[EntrySignal] = []
    for engine in ALL_ENGINES:
        try:
            signal = engine.scan(df, ticker, regime, profile=profile)
            if signal:
                signals.append(signal)
                logger.debug(
                    "[%s] Engine '%s' fired: %s",
                    ticker, engine.name, signal,
                )
        except Exception as e:
            logger.warning(
                "[%s] Engine '%s' error: %s", ticker, engine.name, e
            )
    return signals
