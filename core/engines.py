# ENGINE 3: BUYING ON WEAKNESS (B.O.W.)
# ══════════════════════════════════════════════════════════════════════════════


class BuyingOnWeaknessEngine(BaseEngine):
    """
    Buying on Weakness (B.O.W.) Entry Engine.

    Identifies extreme capitulation and triggers a buy on a
    bullish reversal candle with massive volume.

    Capitulation conditions (one or both):
    - Price < Lower Bollinger Band (20-period, 2 std)
    - RSI(14) < 25

    Entry trigger:
    - Volume climax: today's volume > 200% of 20-day average
    - Bullish reversal candle: close > open (green candle)
    - Close > previous close (higher daily close)

    Priority: 1 (lowest)
    Allowed regimes: ALL (designed for crashes)
    """

    name = "buying_on_weakness"
    priority = 1

    def scan(
        self,
        df: pd.DataFrame,
        ticker: str,
        regime: RegimeSnapshot,
        profile: "StockProfile | None" = None,
    ) -> EntrySignal | None:
        # B.O.W. fires in any regime — no gate check
        if not regime.allows_engine(self.name):
            return None

        if len(df) < max(BOW_BOLLINGER_PERIOD + 5, 30):
            return None

        last = df.iloc[-1]
        prev = df.iloc[-2]
        last_close = float(last["Close"])
        last_open = float(last["Open"])
        prev_close = float(prev["Close"])

        # Adaptive thresholds
        rsi_threshold = profile.rsi_oversold if profile else BOW_RSI_THRESHOLD
        vol_climax_ratio = profile.volume_spike_threshold if profile else BOW_VOLUME_CLIMAX_RATIO

        # ── Capitulation Check ────────────────────────────────────────
        rsi_series = rsi(df["Close"], period=14)
        prev_rsi = float(rsi_series.iloc[-2]) if not pd.isna(rsi_series.iloc[-2]) else 50.0

        upper_bb, middle_bb, lower_bb = bollinger_bands(
            df["Close"], period=BOW_BOLLINGER_PERIOD, num_std=BOW_BOLLINGER_STD
        )
        prev_lower_bb = float(lower_bb.iloc[-2]) if not pd.isna(lower_bb.iloc[-2]) else 0.0

        # Condition 1: Extreme RSI (adaptive)
        capitulation_rsi = prev_rsi < rsi_threshold
        
        # Condition 2: Bollinger Band Capitulation
        capitulation_bb = prev_close < prev_lower_bb

        # Condition 3: StochRSI Oversold Bounce
        stoch_k, stoch_d = stoch_rsi(df["Close"], period=BOW_STOCH_RSI_PERIOD)
        prev_k = float(stoch_k.iloc[-2]) if not pd.isna(stoch_k.iloc[-2]) else 50.0
        prev_d = float(stoch_d.iloc[-2]) if not pd.isna(stoch_d.iloc[-2]) else 50.0
        last_k = float(stoch_k.iloc[-1]) if not pd.isna(stoch_k.iloc[-1]) else 50.0
        last_d = float(stoch_d.iloc[-1]) if not pd.isna(stoch_d.iloc[-1]) else 50.0
        
        capitulation_stoch = (
            prev_k < BOW_STOCH_RSI_OVERSOLD and 
            prev_d < BOW_STOCH_RSI_OVERSOLD and 
            last_k > last_d and  # Bullish cross today
            prev_k <= prev_d     # Was below or equal yesterday
        )

        # Condition 4: MACD Bullish Divergence
        macd_line, signal_line, macd_hist = macd(
            df["Close"], fast=BOW_MACD_FAST, slow=BOW_MACD_SLOW, signal=BOW_MACD_SIGNAL
        )
        
        capitulation_macd = False
        if len(df) > BOW_MACD_DIVERGENCE_LOOKBACK + 5:
            # Lookback window for price and MACD histogram
            lookback_lows = df["Low"].iloc[-(BOW_MACD_DIVERGENCE_LOOKBACK+1):-1]
            lookback_hists = macd_hist.iloc[-(BOW_MACD_DIVERGENCE_LOOKBACK+1):-1]
            
            min_low_idx = lookback_lows.idxmin()
            min_low_val = lookback_lows.min()
            
            # If price made a lower low recently compared to the N-day window
            if prev["Low"] <= min_low_val:
                # Find the histogram value at that previous absolute low
                hist_at_min_low = macd_hist.loc[min_low_idx]
                prev_hist = macd_hist.iloc[-2]
                
                # Divergence: Price is lower/equal, but Histogram is higher (less negative)
                if prev_hist < 0 and prev_hist > hist_at_min_low:
                    capitulation_macd = True

        if not (capitulation_rsi or capitulation_bb or capitulation_stoch or capitulation_macd):
            return None

        # ── Entry Trigger ─────────────────────────────────────────────
        # Volume climax today
        vol_ratio_series = volume_ratio(df, period=20)
        last_vol_ratio = float(vol_ratio_series.iloc[-1]) if not pd.isna(vol_ratio_series.iloc[-1]) else 1.0

        if last_vol_ratio < vol_climax_ratio:
            return None

        # Bullish reversal candle: close > open AND close > prev close
        if not (last_close > last_open and last_close > prev_close):
            return None

        # Signal confirmed
        last_rsi = float(rsi_series.iloc[-1]) if not pd.isna(rsi_series.iloc[-1]) else 50.0
        atr_series = atr(df, period=ATR_PERIOD)
        last_atr = float(atr_series.iloc[-1]) if not pd.isna(atr_series.iloc[-1]) else 0.0

        # Score B.O.W. signals by volume intensity
        score = self.priority * last_vol_ratio

        capitulation_reason = []
        if capitulation_rsi:
            capitulation_reason.append(f"RSI={prev_rsi:.1f}<{rsi_threshold}")
        if capitulation_bb:
            capitulation_reason.append(f"below_lower_BB={prev_lower_bb:.0f}")
        if capitulation_stoch:
            capitulation_reason.append("StochRSI_Cross")
        if capitulation_macd:
            capitulation_reason.append("MACD_Div")

        details = {
            "capitulation": " + ".join(capitulation_reason),
            "prev_rsi": round(prev_rsi, 2),
            "current_rsi": round(last_rsi, 2),
            "volume_ratio": round(last_vol_ratio, 2),
            "prev_close": round(prev_close, 2),
            "lower_bb": round(prev_lower_bb, 2),
            "atr": round(last_atr, 2),
        }
        
        if capitulation_stoch:
            details["stoch_k"] = round(last_k, 2)
            details["stoch_d"] = round(last_d, 2)
            
        if capitulation_macd:
            details["macd_hist"] = round(float(macd_hist.iloc[-1]), 4)

        return EntrySignal(
            engine=self.name,
            ticker=ticker,
            price=round(last_close, 2),
            score=round(score, 2),
            priority=self.priority,
            details=details,
        )


